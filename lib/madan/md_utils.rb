module Madan
  class MdUtils
    def h1(title)
      "# #{title}\n"
    end

    def h2(title)
      "## #{title}"
    end

    def h3(title)
      "### #{title}"
    end

    def h4(title)
      "#### #{title}"
    end

    def h5(title)
      "##### #{title}"
    end

    def hr
      "*** \n"
    end

  end
end